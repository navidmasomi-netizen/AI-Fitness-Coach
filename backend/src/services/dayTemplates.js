export function humanizeDayType(dayType) {
  return dayType
    .split("_")
    .map((part) => {
      if (part === "a" || part === "b" || part === "c") return part.toUpperCase();
      return part.charAt(0).toUpperCase() + part.slice(1);
    })
    .join(" ");
}
